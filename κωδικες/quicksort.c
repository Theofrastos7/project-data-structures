int partition(struct Data d[], int left, int right) {

  int pivot,i,j;

  pivot = d[right].cumulative; // Διαλεγω το πιο δεξια ως pivot
  i = (left - 1); // pointer i που ξεκινάει από την αρχή του πίνακα


  for (j = left; j < right; j++) { // Συγκρινω καθε στοιχειο του πινακα με το pivot
    if (d[j].cumulative < pivot){ // Αν ειναι μικροτερο το στοιχειο απο το pivot τοτε το εναλλαω με μεγαλυτερο στοιχειο απο το pivot που το δειχνει το i.
      i++;
      swap(&d[i], &d[j]); // εναλλαγη στοιχεων του i με του j.
    }

    if(d[j].cumulative == pivot && comparedates(d[j].date, d[right].date) < 0) //Ιδια λογικη για ιδια cumulative με βαση τις ημερομηνιες.
    {
      i++;
      swap(&d[i], &d[j]);
    }
}

  swap(&d[i + 1], &d[right]);// swap του pivot με το μεγαλυτερο στοιχειο στο i.

  return (i + 1);// επιστρεφω τον δεικτη που δειχνει στον pivot.
}

void swap(struct Data d1[], struct Data d2[])
{
    struct Data temp;
    temp = *d1;
    *d1 = *d2;
    *d2 = temp;
}


void quickSort(struct Data d[], int left, int right) { //Συνάρτηση για να βρει τον pivot ώστε τα αριστερά στοιχεία να ειναι μικροτερα του pivot και τα δεξια μεγαλυτερα
    int pivot; //Δείκτης για τον pivot
    if (left < right) {
        pivot = partition(d, left, right);//Ευρεση pivot
        quickSort(d, left, pivot - 1); //ταξινόμηση του αριστερού μέρους του pivot
        quickSort(d, pivot + 1, right); //Ταξινόμηση του δεξιού μέρους του pivot
    }
}


int comparedates(char date1[],char date2[])
{

    int d1,m1,y1,d2,m2,y2;

    // Μετατροπη των strings σε integers για να μπορω να συγκρινω τις ημερομηνίες. Σημείωση: Η strcmp δεν δουλέυει για ημερομηνίες
    sscanf(date1, "%d/%d/%d", &d1, &m1, &y1);
    sscanf(date2, "%d/%d/%d", &d2, &m2, &y2);

    if (y1 > y2) {
        return 1;
    } else if (y1 < y2) {
        return -1;
    } else if (m1 > m2) { // Αν y1 = y2 τότε κοιτάω τους μήνες
        return 1;
    } else if (m1 < m2) {
        return -1;
    } else if (d1 > d2) { // Αν m1 = m2 τότε κοιτάω τις ημέρες
        return 1;
    } else if (d1 < d2) {
        return -1;
    } else {
        return 0;
    }

}


